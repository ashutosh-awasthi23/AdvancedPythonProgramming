# Bitwise Operator

# Bitwise AND (&)
a = 10
b = 5
print("a & b:", a & b)

# Bitwise OR (|)
print("a | b:", a | b)

# Bitwise XOR (^)
print("a ^ b:", a ^ b)

# Bitwise NOT (~)
print("~a:", ~a)


# Left shift (<<)
print("a<<2:", a << 2)

# Right shift (>>)
print("a>>2:", a >> 2)
